package br.com.calculadora;

public class Funcao {

    public int soma(int a, int b) {
        return a + b;
    }

    public int subtracao(int a, int b) {
        return a - b;
    }

    public int multiplicacao(int a, int b) {
        return a * b;
    }

    public float divisao(float a, float b) {
        return a / b;
    }
}
